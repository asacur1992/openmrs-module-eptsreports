package org.openmrs.module.eptsreports.reporting.reports;

import java.io.IOException;
import java.util.Arrays;
import java.util.List;
import java.util.Properties;
import org.openmrs.module.eptsreports.reporting.library.cohorts.GenericCohortQueries;
import org.openmrs.module.eptsreports.reporting.library.datasets.FoundPatientsDataset;
import org.openmrs.module.eptsreports.reporting.library.datasets.PatientsNotFoundDataset;
import org.openmrs.module.eptsreports.reporting.library.queries.BaseQueries;
import org.openmrs.module.eptsreports.reporting.reports.manager.EptsDataExportManager;
import org.openmrs.module.eptsreports.reporting.utils.EptsReportUtils;
import org.openmrs.module.reporting.ReportingException;
import org.openmrs.module.reporting.evaluation.parameter.Mapped;
import org.openmrs.module.reporting.report.ReportDesign;
import org.openmrs.module.reporting.report.definition.ReportDefinition;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;

@Component
public class SetupSearchReport extends EptsDataExportManager {

  @Autowired private FoundPatientsDataset foundPatientsDataset;

  @Autowired private PatientsNotFoundDataset patientsNotFoundDataset;

  @Autowired protected GenericCohortQueries genericCohortQueries;

  @Override
  public String getVersion() {
    return "1.0-SNAPSHOT";
  }

  @Override
  public String getUuid() {
    return "52570324-adac-46c9-9a4c-33ef98cfd65c";
  }

  @Override
  public String getExcelDesignUuid() {
    return "a59e7b8e-9c59-43f5-a4c6-bd4c19af346d";
  }

  @Override
  public String getName() {
    return "Relatório de Pacientes Buscados (Encontrados e Não encontrados)";
  }

  @Override
  public String getDescription() {
    return "MER Quarterly Report";
  }

  @Override
  public ReportDefinition constructReportDefinition() {
    final ReportDefinition reportDefinition = new ReportDefinition();

    reportDefinition.setUuid(this.getUuid());
    reportDefinition.setName(this.getName());
    reportDefinition.setDescription(this.getDescription());
    reportDefinition.setParameters(this.patientsNotFoundDataset.getParameters());

    reportDefinition.addDataSetDefinition(
        "F", Mapped.mapStraightThrough(this.foundPatientsDataset.constructFoundPatientsDataset()));

    reportDefinition.addDataSetDefinition(
        "NF",
        Mapped.mapStraightThrough(this.patientsNotFoundDataset.constructPatientsNotFoundDataset()));

    reportDefinition.setBaseCohortDefinition(
        EptsReportUtils.map(
            this.genericCohortQueries.generalSql(
                "baseCohortQuery", BaseQueries.getBaseCohortQuery()),
            "endDate=${endDate},location=${location}"));

    return reportDefinition;
  }

  @Override
  public List<ReportDesign> constructReportDesigns(final ReportDefinition reportDefinition) {
    ReportDesign reportDesign = null;
    try {
      reportDesign =
          this.createXlsReportDesign(
              reportDefinition,
              "SEARCH_PATIENTS.xls",
              "Relatório de Pacientes Buscados",
              this.getExcelDesignUuid(),
              null);
      final Properties props = new Properties();
      props.put("sortWeight", "5000");
      reportDesign.setProperties(props);
    } catch (final IOException e) {
      throw new ReportingException(e.toString());
    }

    return Arrays.asList(reportDesign);
  }
}
